     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $products = Product::all();
        $simpleProducts = $products->map(function ($product) {
            return [
                'id' => $product->id,
                'name' => $product->name,
                'description' => $product->description,
                'price' => $product->price,
                'category' => $product->category,
                'image' => $product->image,
                'duration' => $product->duration,
            ];
        });

        return response(json_encode($simpleProducts), 200, ['Content-Type' => 'application/json']);
    }

